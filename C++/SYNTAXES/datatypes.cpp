#include<iostream>
using namespace std;
int main() {

    int a;
    cin>>a;

    cout<<"size of int :"<<sizeof(a)<<endl;
    
    float b;
    cin>>b;

    cout<<"Size of float :"<<sizeof(b)<<endl;

    char c;
    cin>>c;
    cout<<"Size of char :"<<sizeof(c)<<endl;
    
    bool d;
    cin>>d;
    cout<<"Size of bool :"<<sizeof(d)<<endl;
    
    short int e;
    cin>>e;
    cout<<"Size of short int :"<<sizeof(e)<<endl;
    
    long int f;
    cin>>f;
    cout<<"Size of long int :"<<sizeof(f)<<endl;

    return 0;
}
